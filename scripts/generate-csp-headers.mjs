#!/usr/bin/env node
/**
 * Copies the hash-based CSP from the prerendered HTML into the nginx header
 * snippet. Run automatically after `vite build` (see package.json).
 */
import { readFileSync, writeFileSync } from "node:fs";

const htmlPath = "build/index.html";
const outPath = "deploy/nginx/security-headers.conf";

const html = readFileSync(htmlPath, "utf8");
const match = html.match(
  /<meta http-equiv="content-security-policy" content="([^"]*)"\s*\/?>/i
);

if (!match) {
  console.error(
    `${htmlPath}: CSP meta tag not found. Is kit.csp enabled in svelte.config.js?`
  );
  process.exit(1);
}

const csp = match[1]
  .replace(/&amp;/g, "&")
  .replace(/&lt;/g, "<")
  .replace(/&gt;/g, ">")
  .replace(/&quot;/g, '"');

if (/script-src[^;]*unsafe-inline/.test(csp)) {
  console.warn("warning: script-src contains unsafe-inline");
}

const nginxValue = csp.replace(/"/g, '\\"');

writeFileSync(
  outPath,
  `# AUTO-GENERATED by scripts/generate-csp-headers.mjs — do not edit by hand.
# Re-run: pnpm build

add_header X-Frame-Options           "SAMEORIGIN"                                   always;
add_header X-Content-Type-Options    "nosniff"                                      always;
add_header Referrer-Policy           "strict-origin-when-cross-origin"              always;
add_header Strict-Transport-Security "max-age=63072000; includeSubDomains; preload" always;
add_header Permissions-Policy        "camera=(), microphone=(), geolocation=()"     always;
add_header Content-Security-Policy   "${nginxValue}" always;
`
);

console.log(`wrote ${outPath}`);
